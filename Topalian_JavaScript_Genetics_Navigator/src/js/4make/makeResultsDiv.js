function makeResultsDiv()
{
    let subDiv = ce('div');
    ge('panelCenter').append(subDiv);

    //-//

    let resultsDiv = ce('div');
    resultsDiv.id = 'results';
    resultsDiv.className = 'result';
    subDiv.append(resultsDiv);
}

